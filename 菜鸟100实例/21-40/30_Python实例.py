# 一个5位数，判断它是不是回文数。即12321是回文数，个位与万位相同，十位与千位相同。
n1 = (input("输入一个五位数:"))
if len(n1) != 5:
    print("请输入一个正确的五位数")
elif n1[-1] == n1[-5] and n1[-2] == n1[-4]:
    print(f"{n1}是一个回文数字")
else:
    print(f"{n1}不是一个回文数字")
